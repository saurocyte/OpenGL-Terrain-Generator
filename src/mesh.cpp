#include <iostream>
#include <mesh.hpp>
#include <initializer_list>
#define STB_IMAGE_IMPLEMENTATION
#include <stb_image.h>
#include <glWindow.hpp>
#include <string>
#include <iterator>

Mesh::Mesh(std::initializer_list<float> _verticies, std::string texturePath)
{
    for (auto curr = begin(_verticies); curr != end(_verticies); std::advance(curr, 5)) {
        float s[] = {curr[0], curr[1], curr[2]};
        verticies.push_back(s);
    }
    // Create VBO
    unsigned int VBO;
    glGenBuffers(1, &VBO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(float) * _verticies.size(), begin(_verticies), GL_STATIC_DRAW);
    // Create VAO
    glGenVertexArrays(1, &VAO);
    glBindVertexArray(VAO);
    // Tell opengl how to interpret vertex data
    // !!! These two functions modify VAO !!!
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);
	glEnableVertexAttribArray(0);
    // Load texture
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float)));
    glEnableVertexAttribArray(1);
    glBindBuffer(GL_ARRAY_BUFFER, 0);
    glGenTextures(1, &textureId);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_REPEAT);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_REPEAT);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
    load(texturePath);
    // Unbind VBO and VAO (VBO state is saved in VAO in previous step)
    glBindVertexArray(0);
}

void Mesh::draw(Window *window) const {
    window -> draw(VAO, verticies.size());
}

void Mesh::load(std::string path) {
    // Load
    int width, height, nrChannels;
    unsigned char *data = stbi_load(path.c_str(), &width, &height, &nrChannels, 0);
    // Bind
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, textureId);
    if (data)
    {
        std::cout << glGetError();
        glTexImage2D(GL_TEXTURE_2D, 0, GL_RGB, width, height, 0, GL_RGBA, GL_UNSIGNED_BYTE, data);
        glGenerateMipmap(GL_TEXTURE_2D);
    }
    else
    {
        std::cout << "Failed to load texture" << std::endl;
    }
    // Free
    stbi_image_free(data);
}

